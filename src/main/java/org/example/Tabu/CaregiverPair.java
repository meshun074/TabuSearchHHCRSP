package org.example.Tabu;

public class CaregiverPair {
    private final int first;
    private final int second;
    public CaregiverPair(int first, int second) {
        this.first = first;
        this.second = second;
    }
    public int getFirst() {
        return first;
    }
    public int getSecond() {
        return second;
    }
}
